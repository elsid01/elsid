package Model;

public class Course {
	//Field	
	private String coursName;
	private String grade;

	//Getter and setters
	public String getCoursName() {
		return coursName;
	}
	public void setCoursName(String coursName) {
		this.coursName = coursName;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}



}
